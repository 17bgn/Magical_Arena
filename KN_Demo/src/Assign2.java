
public class Assign2 {
	public static void method(double a,double b) {
		System.out.println(a%b); }
	public static void method(double a,double b,double c) {
		System.out.println(a+b+c); }
	public static void method(float a,float b) {
		System.out.println(a/b); }
	public static void method(int a,double b,float c) {
		System.out.println(a*b*c); }
	public static void method(String a) {
		System.out.println(a); }
	public static void method(char c) {
		System.out.println(c); }
	public static void method(int a, int b) {
		System.out.println(a+b); }
	public static void method(int a,long b) {
		System.out.println(a-b);}
}
